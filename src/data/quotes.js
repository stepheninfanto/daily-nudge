export const getRandomQuote = (excludeIds = []) => {
  const availableQuotes = quotes.filter(q => !excludeIds.includes(q.id));
  if (availableQuotes.length === 0) return quotes[Math.floor(Math.random() * quotes.length)];
  return availableQuotes[Math.floor(Math.random() * availableQuotes.length)];
};

export const getQuoteById = (id) => quotes.find(q => q.id === id);

export const getQuotesByCategory = (category) => {
  if (category === 'all') return quotes;
  return quotes.filter(q => q.category === category);
};
